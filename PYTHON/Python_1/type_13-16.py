# 아스키 코드
# 컴퓨터는 숫자와 문자에 대한 정보처리를 이진수로 처리하기 때문에 문자와 숫자를 대응시켜 줘야하는데 이런 방법중에 하나인 ASCII규약에 따라 번호를 붙이는 방법을 말한다
# 대문자 A -> 65 (이후에 하나씩 증가) Z -> 90
# 소문자 a -> 97 (이후에 하나씩 증가) z -> 122
# 숫자 0 -> 48 (이후에 하나씩 증가) 9 -> 57
# 엔터(Enter) -> 13
# 아스키 코드 0번 --> NULL


# ord('A') 해당 문자에 해당하는 아스키 값을 반환하는 함수
print(ord('A'))
print(ord('B'))
print(ord('C'))

# chr(65) 해당 아스키 값에 해당하는 문자를 반환하는 함수
print(chr(65))
print(chr(66))
print(chr(67))
